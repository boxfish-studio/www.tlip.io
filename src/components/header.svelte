<script lang="ts">
    import { page } from "$app/stores";
    import { activeSectionId, lightModeNavbar } from "$lib/store";
    import Burger from "./burger.svelte";
    import Button from "./button.svelte";
    import type { Button as ButtonType } from "$lib/types/components";
    export let items = [];
    export { classes as class };

    let classes: string = "";

    let sideMenuOpen: boolean = false;

    const bodyClassList = document.querySelector('body').classList

    const BUTTON: ButtonType = {
        title: "CONTACT US",
        url: "/contact",
        small: true,
        classes: "text-14"
    };

    const toggleMenu: () => void = () => {
        sideMenuOpen ? closeMenu() : openMenu();
    };
    const closeMenu = () => {
        sideMenuOpen = false;
        bodyClassList.remove('overflow-y-hidden')
    };
    const openMenu = () => {
        sideMenuOpen = true;
        bodyClassList.add('overflow-y-hidden')
    };
</script>

<nav
    class="fixed py-1  z-50 w-full bg-blur  text-grey-600 {classes}"
    class:lightMode={$lightModeNavbar && !sideMenuOpen}
>
    <div class="container flex justify-between">
        <a href="/" class="py-4">
            <img src="/assets/logo-TLIP.svg" alt="TLIP logo" id="logo" />
        </a>
        <div class="order-3 my-auto hidden lg:inline-block">
            <Button {...BUTTON} />
        </div>
        <div class="flex flex-row">
            {#if items}
                <!-- Desktop -->
                <ul
                    class="flex-row items-center md:space-x-2 lg:space-x-4 xl:space-x-12 hidden w-full lg:flex transition-opacity duration-200"
                >
                    {#each items as { title, url, id, onClick }}
                        {#if title}
                            <li class="shrink-0">
                                {#if id && url === $page.path}
                                    <a
                                        href={id}
                                        on:click|preventDefault={onClick}
                                        class="{id === '#' + $activeSectionId ? 'highlight' : ''} hover:text-green-400"
                                        >{title}</a
                                    >
                                {:else if url || (id && url != $page.path)}
                                    <a href={url} class="{url === $page.path ? 'highlight' : ''} hover:text-green-400"
                                        >{title}</a
                                    >
                                {/if}
                            </li>{/if}
                    {/each}
                </ul>
                <!-- Burger Icon -- Mobile  -->
                <Burger bind:open={sideMenuOpen} onClick={toggleMenu} {sideMenuOpen} classes="flex my-auto lg:hidden" />
            {/if}
        </div>
    </div>
</nav>

<!-- Mobile Menu   -->
 <aside class="bg-green-200 h-screen w-0 fixed left-0 top-0 lg:hidden z-40 whitespace-nowrap {sideMenuOpen ? 'open' : ''}">
                    <ul
                        class="container h-screen pt-20 text-black border-t-2 w-full transition-opacity duration-200 {!sideMenuOpen
                            ? 'opacity-0'
                            : 'opacity-100'}"
                    >
                        {#each items as { title, url, id, onClick }}
                            {#if title}
                                <li class="py-4 nav-link min-w-max">
                                    {#if id && url === $page.path}
                                        <a
                                            class={id === "#" + $activeSectionId ? "highlight" : ""}
                                            href={id}
                                            on:click|preventDefault={e => {
                                                onClick(e);
                                                closeMenu();
                                            }}>{title}</a
                                        >
                                    {:else if url || (id && url != $page.path)}
                                        <a class={url === $page.path ? "highlight" : ""} href={url}>{title}</a>
                                    {/if}
                                </li>
                            {/if}
                        {/each}
                    </ul>
                </aside>

<style lang="scss">
    aside {
            transition: width 0.3s ease-in-out;
            &.open {
                @apply w-screen;
            }
        }
    nav {
        a {
            @apply transition-colors;
            @apply duration-150;
        }
        .highlight {
            @apply font-bold;
        }
        &.bg-blur {
            @apply bg-grey-100;
            @apply bg-opacity-20;
        }
        /* if backdrop support: transparent and blurred */
        @supports ((-webkit-backdrop-filter: blur(4px)) or (backdrop-filter: blur(4px))) {
            &.bg-blur {
                @apply bg-transparent;
                -webkit-backdrop-filter: blur(4px);
                backdrop-filter: blur(4px);
            }
        }
        &.lightMode {
            @apply text-white;
            #logo {
                filter: invert(100%) sepia(100%) saturate(36%) hue-rotate(279deg) brightness(109%) contrast(112%);
            }
        }
    }
</style>
